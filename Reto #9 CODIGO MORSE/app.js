const phrase = 'Hola estoy haciendo un transcriptor de codigo morse'

const transcriptor = (phrase) => {
    const arr = phrase.toLowerCase().split('')
    let setenceInMorse = ''
    arr.forEach(word => {
        
        if (word === ' ') setenceInMorse += '  '
        if (alfabeto[word])
            setenceInMorse += alfabeto[word].concat(' ')
    });
    return setenceInMorse
}

